    public void Inisiasi(String rl,String nm){
        try {
            switch(rl){
                case "1":
                    nama.setText(nm);
                    btnBuku.setEnabled(true);
                    btnAnggota.setEnabled(true);
                     jPanel.add(b).setVisible(true);
                    btnAdmin.setEnabled(true);
                    break;
                case "2":
                    nama.setText(nm);
                     jPanel.add(pmj).setVisible(true);
                    btnBuku.setEnabled(false);
                    btnAnggota.setEnabled(false);
                    btnAdmin.setEnabled(false);
                    break;
                default:
                    break;
            }
        } catch (Exception e) {
        }
        
    }

