    private void autoNumber()  {
        String noMhs = "MHS000";
        int i = 0;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/e_library","root","");
            s = (com.mysql.jdbc.Statement) conn.createStatement();
            String sql = "select kd_mhs from mahasiswa";
            ResultSet rs = s.executeQuery(sql);
            while(rs.next()){
                noMhs = rs.getString("kd_mhs");
            }
            noMhs = noMhs.substring(3);
            i = Integer.parseInt(noMhs)+1;
            noMhs = "00"+i;
            noMhs = "MHS"+noMhs.substring(noMhs.length()-3);
            txtkdmhs.setText(noMhs);
        } catch (SQLException e) {
            JOptionPane.showConfirmDialog(this, e);
        } catch (ClassNotFoundException ex) {
              Logger.getLogger(BukuForm.class.getName()).log(Level.SEVERE, null, ex);
          }
        
    }
    private void showData(){
         DefaultTableModel model = new DefaultTableModel();
         model.addColumn("Kode Mahasiswa");
         model.addColumn("Nama");
         model.addColumn("Username");
         model.addColumn("Fakulitas");
         model.addColumn("Jurusan");
         model.addColumn("Gambar Path");
         
         try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = (com.mysql.jdbc.Connection)  DriverManager.getConnection("jdbc:mysql://localhost/e_library","root","");
            s = (com.mysql.jdbc.Statement) conn.createStatement();
            String sql = "select *from mahasiswa order by id_mahasiswa asc ";
             ResultSet rs = s.executeQuery(sql);
             
             while (rs.next()) {                 
                 model.addRow(new Object[]{
                     rs.getString("kd_mhs"),
                     rs.getString("nama_mhs"),
                     rs.getString("username"),
                     rs.getString("fakulitas"),
                     rs.getString("jurusan"),
                      rs.getString("nama_image"),
                     rs.getString("image_path")
                 });
             }
             TMahasiswa.setModel(model);
        } catch (Exception e) {
            System.out.println("eror" + e.getMessage());
            e.printStackTrace();
        }
         
          try {
            if (conn != null) {
                conn.close();
                s.close();
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
